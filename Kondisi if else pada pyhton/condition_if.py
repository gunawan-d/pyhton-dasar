x = 10
y = 20

# Karena x lebih kecil dari y adalah benar, maka kode di dalam scope statement if akan dijalankan.
if(x < y):
    print("nilai x lebih kecil dari y.") # ini akan dijalankan

# karena x lebih kecil dari y adalah salah, maka kode di dalam scope statement if tidak akan dijalankan.
if(x > y):
    print("nilai x lebih besar dari y.") #ini tidak akank dijalankan